public class Hrac {

    private String jmeno;
    private char symbol;

    public Hrac(String jmeno, char symbol) {
        this.jmeno = jmeno;
        this.symbol = symbol;
    }

    public String getJmeno() {
        return jmeno;
    }

    public void setJmeno(String jmeno) {
        this.jmeno = jmeno;
    }

    public char getSymbol() {
        return symbol;
    }

    public void setSymbol(char symbol) {
        this.symbol = symbol;
    }

}
